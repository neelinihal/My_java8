public class StatementsWhiteSpace {
    public static void main(String[] args) {
        int iii = 40;
        iii++;
        System.out.println(iii);

        System.out.println("his " +                       // This are all statements which we can write in single or multiple lines
                "name " +                                 // statements are ended with ;
                "is nihal");

        int mani = 50;
        mani++;
        System.out.println("mani");
    }
}
